// Задайте прямоугольный двумерный массив. Напишите программу, 
// которая будет находить строку с наименьшей суммой элементов.

// Например, задан массив:
// 1 4 7 2
// 5 9 2 3
// 8 4 2 4
// 5 2 6 7
// Программа считает сумму элементов в каждой строке и выдаёт номер строки с наименьшей суммой элементов: 1 строка


// Инициализация двумерного массива
            int[,] array = new int[,]
            {
                {1, 4, 7, 2},
                {5, 9, 2, 3},
                {8, 4, 2, 4},
                {5, 2, 6, 7}
            };

            int minSum = int.MaxValue; // Переменная для хранения минимальной суммы
            int minSumIndex = -1; // Переменная для хранения индекса строки с минимальной суммой

            // Перебираем каждую строку в массиве
            for (int i = 0; i < array.GetLength(0); i++)
            {
                int sum = 0; // Переменная для хранения суммы элементов строки

                // Перебираем каждый элемент в строке
                for (int j = 0; j < array.GetLength(1); j++)
                {
                    sum += array[i, j]; // Добавляем элемент к сумме
                }

                // Проверяем, является ли сумма минимальной
                if (sum < minSum)
                {
                    minSum = sum; // Обновляем минимальную сумму
                    minSumIndex = i; // Обновляем индекс строки с минимальной суммой
                }
            }

            // Выводим номер строки и наименьшую сумму элементов
            Console.WriteLine("Номер строки с наименьшей суммой элементов: " + (minSumIndex + 1));
            Console.WriteLine("Наименьшая сумма элементов: " + minSum);